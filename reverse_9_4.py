# Система восстановления потоков "ФИКС-4"
# Анализатор байтовых помех
# ВНИМАНИЕ: Обнаружена помеха в байт-потоке

def main():
    # Поврежденные данные со смещением
    ENC = [
        122, 119, 115, 119, 108, 127, 102, 125, 120, 55, 99, 119, 108, 53, 106, 120, 99, 106, 109, 124, 129
    ]
    
    print("Система восстановления ФИКС-4")
    print("Введите восстановленные данные:")
    
    try:
        user_input = input().strip()
    except EOFError:
        return
    
    if len(user_input) != len(ENC):
        print("ОШИБКА: Неверная длина данных!")
        print("СИСТЕМА ЗАБЛОКИРОВАНА")
        return
    
    # Корректируем помехи в потоке
    # Компенсируем смещение на 4 единицы
    decrypted = ""
    for encrypted_byte in ENC:
        # Восстанавливаем исходный байт
        decrypted_char = chr((encrypted_byte - 4) & 0xFF)
        decrypted += decrypted_char
    
    if user_input == decrypted:
        print("ДОСТУП РАЗРЕШЕН")
        print("ПОТОК ВОССТАНОВЛЕН")
        print("ФИКС-4: Помехи устранены успешно")
    else:
        print("ОШИБКА: Данные некорректны!")
        print("ДОСТУП ЗАПРЕЩЕН")

if __name__ == "__main__":
    main()